<h2 align="center" style="color:red">
<img src="../quarantine/photos/victoria_nelson.gif" width="316" height="380" align="left" style="margin-right:10px;" />Victoria Nelson</h2>
<h2 align="center" style="color:blue">A Postcool Everywoman</h2>
  <br />
<h3 align="center" style="color:red">Thursday, October 11 at 7:30 pm<br />
The Spirit Room<br />
111 Broadway, Fargo</h3>
<br />
The NDSU Library and the College of Arts, Humanities, and Social Sciences are
pleased to announce the first in a projected series of lectures by independent
scholars. Victoria Nelson is a writer of fiction and nonfiction. Her books include
Wild California (short stories); The Secret Life of Puppets (a study of the supernatural
grotesque in Western culture); On Writer's Block (a study of creativity from
its least popular aspect); and a memoir, My Time in Hawaii. She was co-translator,
with Walter Arndt, of The Letters, Drawings, and Essays of Bruno Schulz. On Writer's
Block was a Book of the Month Club alternate selection; The Secret Life of Puppets
won the 2002 Modern Language Association's Scaglione Prize in Comparative Literary
Studies. She teaches in the Goddard College MFA Creative Writing Program at Port
Townsend, WA.<br>
<br>
Her lecture, Faux Catholic: Religion in 20th Century Secular Literature [A Gothic
Subgenre from Monk Lewis to Dan Brown], is scheduled for Wednesday, October 10,
at 7:00-8:00 p.m., in Minard Room 219, NDSU. In this talk, Nelson analyzes the
fantasy pop culture religion she calls &quot;faux Catholicism.&quot; Drawn from
movies such as The Exorcist and popular fiction such as The Da Vinci Code, Faux
Catholicism and its magical talismans (as opposed to the Church's real-life theological
doctrine) provide the only effective defense against the forces of evil as embodied
in the Judeo-Christian figure of Satan.<br>
<br>
On Thursday, October 11 at 7:30-8:30 pm, at the Spirit Room (111 Broadway, Fargo),
Nelson will speak on A Postcool Everywoman. The subject is a play about a suburban
matron's quest to hold onto her beauty and stave off the aging process, which
takes her from life to death and the underworld. This lecture/video presents
a California theater group's Day of the Dead performance piece which reinvents
the medieval morality play EVERYMAN using mime, puppets, and the Marin County,
Calif., Recycling Center.<br />
<br />
<span style="color:red">These events are free and open to the public<br />
<br />
Sponsored by:</span><span style="color:blue;"> NDSU Libraries, College of Arts, Humanities and Social Sciences,
Department of English, and the Vice President for Research, Creative Activities,
and Technology Transfer</span>
