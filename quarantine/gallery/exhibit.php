    <td align="center"><p><strong>Guidelines for Gallery Exhibitions at the Spirit
          Room</strong></p>
      <p align="left">Normally, a potential exhibitor is recommended to the Spirit Room Galleries
        by someone in the local arts community, most often someone in visual
        arts academia.&nbsp; It is rare that an acclaimed artist would contact
        us directly.&nbsp; Sometimes, a gallery will contact the Spirit Room
        recommending an artist that they have shown recently.&nbsp; In some cases,
        we have exhibits traveling through the North Dakota Association of Art
        Galleries.&nbsp; In other cases, a grant application is written to bring
        the works of a well-known artist to the gallery such as the Bremer Foundation
        underwriting Bright Bird: the art of Sister Corita Kent.&nbsp; Another
        type of exhibit tied to grant funding was Carving a New Life: Izudin
        Becic, from the Bosnian wood carving tradition, an exhibit underwritten
        by the National Endowment for the Arts.</p>
      <p align="left">Exhibitors are asked to submit visual images electronically or in some
        cases they will ask to bring examples of their work into the Spirit Room
        offices.&nbsp; An interview is conducted and the curator makes a decision
        about whether an exhibit will be offered to the artist.</p>
      <p align="left">Several factors come into play because artists come from various backgrounds.
        Generally speaking, we like to have recently completed works using the
        guideline of work produced within the last three years.</p>
      <p align="left">Because the Spirit Room has two galleries, larger work and more prolific
        artists exhibit in the larger gallery space.&nbsp; The smaller gallery
        is especially good for young artists who may not have a large body of
        work or artists that would have difficulty with the costs of framing.</p>
      <p align="left">The work is scheduled, at times, as far out as six months to a year
        ahead of time.&nbsp; That means that the artists usually must plan ahead
        to both have the work completed and be available at the time of the exhibition.</p>
      <p align="left">Biographies, artist&rsquo;s statements and visual images are required
        to be submitted a month before the opening of the exhibit so that the
        press can become involved in the promotion of the exhibit.</p>
