    <p><strong>The Great Winter Crow Show at the Spirit Room, </strong>held in
      alternate years<strong>,</strong> is a non-competitive festive exhibition
      free of entry fees.&nbsp;&nbsp; It is intended to be a mad combining of
      energies and visual ingredients. The theme is the crow, the blackbird,
      and all their marvelous and malevolent associations.&nbsp; The crows and
      ravens keep us company in the wintertime and continue to remind us with
      their squawks and pacing that they keep a close eye on our behaviors and
      movements. </p>
<div style="float:right; margin:5px;">
	<img src="quarantine/updates/CrowShow2010.jpg" width="250" /></div>    <p>Crows are known to play catch with twigs, to slide down snowy hills on
      their backs head-first, to soar and dive for entertainment, to carry shiny
      objects back to their nests for contemplation, and to collect hallucinogenic
      seeds in the fall, saving them to bring color to winter-white boredom.
      It gets slow in the winter for people too, a good reason to celebrate &ldquo;the
      uncanny crow.&rdquo;</p>
    <p>The Reverend Henry Ward Beecher said in the mid 1880s, &ldquo;If men had
      wings and bore black feathers, few of them would be clever enough to match
      the crow.&rdquo;</p>
    <p>This is a chance to put on your thinking cap and share your talents and
      good humor by entering the 
      Great Winter Crow Show.</p>
