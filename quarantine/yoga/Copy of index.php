<h2 align="center">Styles of Yoga</h2>
<div align="center"><strong>By Dawn Morgan</strong> </div>
<p><strong>There are various schools of yoga. Practitioners new to yoga can try 
  different styles to determine which approach works for them. New students will 
  find that even within a certain style of yoga, individual teachers vary greatly. 
  They will also find that one style may be good now and may change completely 
  later on. There are also varying degrees of complexity within the styles, resulting 
  in children's classes, maternity classes and senior's classes. There are also 
  beginning through advanced classes within the various styles.</strong></p>
<p><strong>Most schools follow basic elements of yoga but vary according to the 
  influence of a particular teacher. Hatha or Physical Yoga is one of eight limbs 
  of yoga that date back thousands of years in the history of India. The eight 
  limbs of yoga are systems of discipline and personal development practiced by 
  millions of Indians historically. The four most common Hatha Yoga schools are 
  Iyengar Yoga, Sivananda Yoga, Ashtanga Yoga and Kundalini Yoga.</strong></p>
<p><strong>Iyengar Yoga is taught by B.K.S. Iyengar, who was born in India in 
  1918 and still lives there today. Students travel to India and stay for long 
  periods of study, sometimes years, to learn his approach. Traditionally, yoga 
  has been taught master to student without written texts. Iyengar's book, Light 
  on Yoga, published in 1966, has enabled yoga to spread all over the world and 
  has been translated into eighteen languages. Iyengar Yoga is precise and emphasizes 
  angular postures and the use of props such as straps, blocks and ropes to move 
  the body into the asanas (positions). The asanas are held for increasingly longer 
  periods of time as the practitioner advances in the discipline.</strong></p>
<p><strong>Sivananda Yoga was developed by a medical doctor, Swami Sivananda, 
  born in South India in 1887. Swami Shishni Denanagda introduced Sivananda's 
  style of yoga to the West in the 1950's. Sivananda Yoga is based on 12 key postures, 
  sun salutations, breathing techniques, meditation and chanting. Because Sivananda 
  Yoga works with variations on the basic postures, it is one of the most versatile 
  and easily adaptable styles to various levels of physical development. Classes 
  can be easily adapted to the student, graduating from a gentle to a rigorous 
  approach.</strong></p>
<p><strong>Ashtanga Yoga, taught by Sri K. Pattabhi Jois, is based on an ancient 
  manuscript called the Yoga Korunta. It is a system of Hatha Yoga, thought to 
  be the practice intended by Patanjali in the Yoga Sutras. This text was deciphered 
  and organized by Pattabhi Jois in the 1930's and is currently one of the most 
  popular styles of yoga worldwide. This style of yoga is rigorous and athletic 
  and may be the most demanding style of yoga. Beginners can work with individual 
  postures before progressing toward the Primary Series and onward.</strong></p>
<p><strong>Kundalini Yoga is based on the teachings of the Sikh master, Yogi Bhajan 
  who introduced the system to the West in 1969 and founded the 3HO Organization 
  (Healthy, Happy, Holy Organization) here in the United Sates. This form of Yoga 
  makes use of breathing techniques, particularly the "Breath of Fire" while holding 
  postures. It focuses on hand and fingers gestures, body locks, chanting, singing 
  and meditation. Kundalini Yoga aims to awaken the energy reserves at the base 
  of the spine and raise this energy up through the chakras (energy centers) along 
  the spine to enhance awakening to greater levels of energy, clarity and joy.</strong></p>
<p><strong>Vinyasa Yoga is sequenced yoga postures with the primary focus on the 
  breath. The breath leads the way from one posture to the next, therefore, the 
  mind must know exactly where the breath is to flow evenly. Another aspect of 
  Vinyasa practice is observation of mental discourse and mental patterning. Students 
  concentrate the mind in the present, allowing the mind to become calm and steady. 
  Vinyasa Yoga carefully balances each asana with counter postures. The sequences 
  change as the physical body changes with the practice. Vinyasa Yoga emphasizes 
  individual instruction. More advanced Vinyasa sequences are combined with sacred 
  prayers and mantras. The use of prayers and mantras usually begins after many 
  years of yoga sequencing when the element of earth is tamed, the physiological 
  body has been calmed and the mind remains passive throughout the yoga sequences. 
  </strong></p>
<p> <strong>Anusara Yoga was founded by John Friend in 1997 and emphasizes the 
  meaning of the word "Anusara," as "Flowing with Grace." Practitioners focus 
  on opening the heart and accepting all experience as divine: sensation, perception, 
  emotion and thought. The asanas are seen as a dance of "flowing with Supreme 
  Consciousness". Therefore, light-heartedness, play and joyful creativity are 
  part of the yoga practice. Anusara Yoga teaches five Universal Principles of 
  Alignment, with study of energetic loops and spirals within the body that help 
  in finding the best form for each individual. </strong></p>
<p> <strong>Kripalu Yoga has been developed at the Kripalu Center for Yoga and 
  Health in Massachusetts. Kripalu is the largest retreat center for Yoga and 
  healing in the United States. A non-profit organization begun thirty years ago, 
  Kripalu has brought yoga into mainstream locations such as schools, senior citizen 
  centers, hospitals and prisons. Kripalu Yoga would be described as a gentle 
  yoga.</strong></p>
<p> <strong>Atma Yoga was developed by Christopher Baxter following thirty years 
  of teaching yoga at Kripalu. Having been trained in Iyengar Yoga and following 
  years of successive injuries from his precise and angular yoga practice, he 
  developed a style of yoga that combines postures based upon "core lift" (use 
  of the bondas) for central stability, and then adding breath and fluid movement. 
  For example, the tree balance posture is grounded with the roots into the earth. 
  Fluidity is added with movement of the branches. Stability, movement and joyfulness 
  are combined in Atma Yoga practice.</strong></p>
<p><strong>Vajrasana Yoga is the style of yoga that evolved for Dawn Morgan of 
  the Spirit Room. Dawn has completed six levels of Shambhala Meditation Training 
  and has led meditation sessions at the Spirit Room for seven years. Beginning 
  yoga practice in the late sixties, she received a certification in Atma Yoga 
  in 2002. Dawn has combined Hatha Yoga, which is traditionally Hindu or Sikh, 
  with elements of Buddhist mindfulness training. Bringing body, mind and emotion 
  together encourages resting in the present and letting go of busyness and tension. 
  Vajrasana Yoga is practiced by relaxing with the outbreath, allowing discursive 
  thought to be released from the mind and rigidity released from the body. When 
  thoughts take one away from the present, the practitioner gently comes back 
  to the body and the breath. Spatial awareness and lack of inherent solidity 
  are explored within the body, the mind and the world. Practitioners are encouraged 
  to take the practice into daily life. By being stable, steady and present in 
  the world, assuming a posture of dignity and openness, we uplift ourselves and 
  others. Vajrasana Yoga is Contemplative Yoga.</strong></p>
