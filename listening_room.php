	    voice.&rdquo;</p>
	  <p>Last summer, The Listening Room tested the waters with Texas musician,
	    Eric Taylor,<br />
	    ( bluerubymusic.com). The Taylor concert was well received.&nbsp; The concert
	    was arranged by Scott Curfman. Curfman has been a fan of Taylor since they
	    met in Houston in 1974.&nbsp; Curfman ran sound for six of Taylor&rsquo;s
	    concerts over the last few years. and he said something happen at The Listening
	    Room concert that he had never seen before: Taylor returned to the stage
	    for a 20 minute encore. Both Taylor and the concert audience said something
	    special happened that night. That night Taylor vowed to return to the Spirit
	    Room in May 2009.</p>
